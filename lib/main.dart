  runApp(App()); // 앱 시작점, 여기에 넣고 실행하는 위젯은 root 위젯이다.
  /**
   * 플러터는 widget 을 레고블럭으로 보고 그 블럭을 합쳐서 결과물을 만드는 것 과 같다!
